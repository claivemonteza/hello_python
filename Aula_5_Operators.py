# Operator_Precedence
x = 10 + 3 * 2
print(x)

x = (10 + 3) * 2
print(x)

# Comparison operators

## Greater than
x = 3 > 2
print(x)

## Less than
x = 3 < 2
print(x)

## Less than or equal to
x = 3 >= 2
print(x)

## Greater than or equal to
x = 3 <= 2
print(x)

## Equal to
x = 3 == 2
print(x)

## Not equal
x = 3 != 2
print(x)

#Logical Operators

## AND
price = 25
print(price > 10 and price < 30)

## OR
price = 5
print(price > 10 or price < 30)

## NOT
price = 5
print(not price > 10)

